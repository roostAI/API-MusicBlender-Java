import org.junit.jupiter.api.BeforeAll;
import org.junit.jupiter.api.TestInstance;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.MethodSource;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.core.type.TypeReference;
import java.io.File;
import java.io.IOException;
import java.util.*;
import java.util.stream.Stream;

@TestInstance(TestInstance.Lifecycle.PER_CLASS)
public class AstronomyJsonGetTest {

	private static final Logger logger = LoggerFactory.getLogger(AstronomyJsonGetTest.class);

	private UnifiedAuthHandler authHandler;

	private ResponseValidator responseValidator;

	private ObjectMapper objectMapper;

	private static final String BASE_URI = "https://api.weatherapi.com/v1";

	private static final String ENDPOINT = "/astronomy.json";

	private static final String HTTP_METHOD = "get";

	private static final String TEST_DATA_FILE = "src/test/java/com/medeiros/RoostTest/weather/datasets/astronomy.jsonGetTest.json";

	private Map<String, ParameterInfo> parameters;

	private SecurityInfo security;

	private Map<Integer, ResponseSchema> schemas;

	@BeforeAll
	public void setup() {
		authHandler = new UnifiedAuthHandler();
		responseValidator = new ResponseValidator();
		objectMapper = new ObjectMapper();

		String baseUrl = authHandler.getBaseUrl();
		if (baseUrl == null || baseUrl.isEmpty()) {
			RestAssured.baseURI = BASE_URI;
		}
		else {
			RestAssured.baseURI = baseUrl;
		}

		parameters = getParameters();
		security = getSecurityInfo();
		schemas = getResponseSchemas();

		logger.info("✓ Test setup completed. Base URI: {}", RestAssured.baseURI);
	}

	public Stream<TestScenario> testScenarioProvider() {
		List<Map<String, Object>> testCases = loadTestData();
		if (testCases == null || testCases.isEmpty()) {
			logger.warn("✗ No test cases found in: {}", TEST_DATA_FILE);
			return Stream.empty();
		}

		int[] index = { 0 };
		return testCases.stream().map(testCase -> {
			index[0]++;
			String scenarioId = String.valueOf(index[0]);
			String scenarioDescription = testCase.containsKey("scenario") ? String.valueOf(testCase.get("scenario"))
					: "Test case " + scenarioId;
			String testName = generateTestName(scenarioDescription, testCase);
			return new TestScenario(scenarioId, testName, scenarioDescription, testCase);
		});
	}

	private String generateTestName(String scenario, Map<String, Object> testCase) {
		Object statusCodeObj = testCase.get("expected_statusCode");
		int statusCode = statusCodeObj != null ? ((Number) statusCodeObj).intValue() : 0;

		String sanitized = scenario.replaceAll("[^a-zA-Z0-9\\s]", "").replaceAll("\\s+", "_").toLowerCase();

		if (sanitized.length() > 50) {
			sanitized = sanitized.substring(0, 50);
		}

		return "test_" + sanitized + "_" + statusCode;
	}

	@ParameterizedTest(name = "[{index}] {0}")
	@MethodSource("testScenarioProvider")
	public void testEndpoint(TestScenario scenario) {
		logger.info("▶ Starting test: {} - {}", scenario.scenarioId, scenario.testName);

		Map<String, Object> testCase = scenario.testCase;
		Object statusCodeObj = testCase.get("expected_statusCode");

		if (statusCodeObj == null) {
			logger.error("✗ Missing expected_statusCode in test case: {}", scenario.scenarioId);
			throw new AssertionError("Missing expected_statusCode in test case");
		}

		int expectedStatusCode = ((Number) statusCodeObj).intValue();
		String authValidityType = (expectedStatusCode == 401) ? "invalid" : "valid";

		logger.info("  Expected status: {}, Auth type: {}", expectedStatusCode, authValidityType);

		Response response = executeTest(testCase, parameters, security, authValidityType);

		ResponseSchema schema = schemas.get(expectedStatusCode);
		if (schema == null) {
			schema = schemas.get(0);
		}

		String contentType = response.getHeader("Content-Type");

		if (contentType != null && contentType.contains("application/json")) {
			responseValidator.validateResponse(response, expectedStatusCode, schema);
			logger.info("  ✓ Response validation completed");
		}
		else {
			if (response.getStatusCode() == expectedStatusCode) {
				logger.info("  ✓ Status code matches: {}", expectedStatusCode);
			}
			else {
				throw new AssertionError("Status code mismatch. Expected: " + expectedStatusCode + ", Actual: "
						+ response.getStatusCode());
			}
		}

		logger.info("✓ Test passed: {} - {}", scenario.scenarioId, scenario.testName);
	}

	private Response executeTest(Map<String, Object> testCase, Map<String, ParameterInfo> parameters,
			SecurityInfo security, String authValidityType) {
		Map<String, Object> queryParams = new HashMap<>();
		Map<String, Object> pathParams = new HashMap<>();
		Map<String, Object> headerParams = new HashMap<>();

		Set<String> skipFields = new HashSet<>(Arrays.asList("scenario", "expected_statusCode", "ScenarioID",
				"TestName", "Description", "Execution", "Validation"));

		for (Map.Entry<String, Object> entry : testCase.entrySet()) {
			String key = entry.getKey();
			Object value = entry.getValue();

			if (skipFields.contains(key)) {
				continue;
			}

			if (value == null) {
				continue;
			}

			if ("headers".equals(key) && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> headers = (Map<String, Object>) value;
				for (Map.Entry<String, Object> headerEntry : headers.entrySet()) {
					if (headerEntry.getValue() != null) {
						headerParams.put(headerEntry.getKey(), headerEntry.getValue());
					}
				}
				continue;
			}

			if ("query".equals(key) && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> query = (Map<String, Object>) value;
				for (Map.Entry<String, Object> queryEntry : query.entrySet()) {
					if (queryEntry.getValue() != null) {
						queryParams.put(queryEntry.getKey(), queryEntry.getValue());
					}
				}
				continue;
			}

			if ("path".equals(key) && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> path = (Map<String, Object>) value;
				for (Map.Entry<String, Object> pathEntry : path.entrySet()) {
					if (pathEntry.getValue() != null) {
						pathParams.put(pathEntry.getKey(), pathEntry.getValue());
					}
				}
				continue;
			}

			ParameterInfo paramInfo = parameters.get(key);
			if (paramInfo != null) {
				String location = paramInfo.getLocation();
				switch (location) {
					case "query":
						queryParams.put(key, value);
						break;
					case "path":
						pathParams.put(key, value);
						break;
					case "header":
						headerParams.put(key, value);
						break;
					default:
						queryParams.put(key, value);
						break;
				}
			}
			else {
				queryParams.put(key, value);
			}
		}

		RequestSpecification request = given().contentType(ContentType.JSON);

		if (security != null && security.getSchemeName() != null) {
			request = authHandler.applyAuth(request, security.getSchemeName(), authValidityType);
		}

		if (!queryParams.isEmpty()) {
			request = request.queryParams(queryParams);
		}

		if (!pathParams.isEmpty()) {
			request = request.pathParams(pathParams);
		}

		if (!headerParams.isEmpty()) {
			for (Map.Entry<String, Object> header : headerParams.entrySet()) {
				request = request.header(header.getKey(), header.getValue());
			}
		}

		logger.info("  Executing {} {} with query params: {}", HTTP_METHOD.toUpperCase(), ENDPOINT, queryParams);

		Response response = request.when().get(ENDPOINT);

		logger.info("  Response status: {}", response.getStatusCode());

		return response;
	}

	private Map<String, ParameterInfo> getParameters() {
		Map<String, ParameterInfo> params = new HashMap<>();
		params.put("q", new ParameterInfo("q", "query", "string", true));
		params.put("dt", new ParameterInfo("dt", "query", "string", true));
		return params;
	}

	private SecurityInfo getSecurityInfo() {
		return new SecurityInfo("ApiKeyAuth", "apiKey", "key", "query");
	}

	private Map<Integer, ResponseSchema> getResponseSchemas() {
		Map<Integer, ResponseSchema> schemasMap = new HashMap<>();

		// 200 Response Schema
		ResponseSchema schema200 = new ResponseSchema();
		Map<String, FieldConstraint> fields200 = new HashMap<>();

		// location object
		FieldConstraint locationField = new FieldConstraint("object");
		Map<String, FieldConstraint> locationNested = new HashMap<>();
		locationNested.put("name", new FieldConstraint("string"));
		locationNested.put("region", new FieldConstraint("string"));
		locationNested.put("country", new FieldConstraint("string"));
		locationNested.put("lat", new FieldConstraint("number"));
		locationNested.put("lon", new FieldConstraint("number"));
		locationNested.put("tz_id", new FieldConstraint("string"));
		locationNested.put("localtime_epoch", new FieldConstraint("integer").setFormat("int32"));
		locationNested.put("localtime", new FieldConstraint("string"));
		locationField.setNestedFields(locationNested);
		fields200.put("location", locationField);

		// astronomy object
		FieldConstraint astronomyField = new FieldConstraint("object");
		Map<String, FieldConstraint> astronomyNested = new HashMap<>();

		FieldConstraint astroField = new FieldConstraint("object");
		Map<String, FieldConstraint> astroNested = new HashMap<>();
		astroNested.put("sunrise", new FieldConstraint("string"));
		astroNested.put("sunset", new FieldConstraint("string"));
		astroNested.put("moonrise", new FieldConstraint("string"));
		astroNested.put("moonset", new FieldConstraint("string"));
		astroNested.put("moon_phase", new FieldConstraint("string"));
		astroNested.put("moon_illumination", new FieldConstraint("string"));
		astroField.setNestedFields(astroNested);

		astronomyNested.put("astro", astroField);
		astronomyField.setNestedFields(astronomyNested);
		fields200.put("astronomy", astronomyField);

		schema200.setFields(fields200);
		schemasMap.put(200, schema200);

		// 400 Response Schema
		ResponseSchema schema400 = new ResponseSchema();
		Map<String, FieldConstraint> fields400 = new HashMap<>();
		fields400.put("code", new FieldConstraint("integer").setFormat("int32"));
		fields400.put("message", new FieldConstraint("string"));
		schema400.setFields(fields400);
		schemasMap.put(400, schema400);

		// 401 Response Schema
		ResponseSchema schema401 = new ResponseSchema();
		Map<String, FieldConstraint> fields401 = new HashMap<>();
		fields401.put("code", new FieldConstraint("integer").setFormat("int32"));
		fields401.put("message", new FieldConstraint("string"));
		schema401.setFields(fields401);
		schemasMap.put(401, schema401);

		// 403 Response Schema
		ResponseSchema schema403 = new ResponseSchema();
		Map<String, FieldConstraint> fields403 = new HashMap<>();
		fields403.put("code", new FieldConstraint("integer").setFormat("int32"));
		fields403.put("message", new FieldConstraint("string"));
		schema403.setFields(fields403);
		schemasMap.put(403, schema403);

		return schemasMap;
	}

	private List<Map<String, Object>> loadTestData() {
		try {
			File file = new File(TEST_DATA_FILE);
			if (!file.exists()) {
				logger.error("✗ Test data file not found: {}", TEST_DATA_FILE);
				return new ArrayList<>();
			}
			return objectMapper.readValue(file, new TypeReference<List<Map<String, Object>>>() {
			});
		}
		catch (IOException e) {
			logger.error("✗ Failed to load test data from {}: {}", TEST_DATA_FILE, e.getMessage());
			return new ArrayList<>();
		}
	}

	private static class TestScenario {

		private final String scenarioId;

		private final String testName;

		private final String description;

		private final Map<String, Object> testCase;

		public TestScenario(String scenarioId, String testName, String description, Map<String, Object> testCase) {
			this.scenarioId = scenarioId;
			this.testName = testName;
			this.description = description;
			this.testCase = testCase;
		}

		@Override
		public String toString() {
			return scenarioId + " - " + testName;
		}

	}

	private static class ParameterInfo {

		private final String name;

		private final String location;

		private final String type;

		private final boolean required;

		public ParameterInfo(String name, String location, String type, boolean required) {
			this.name = name;
			this.location = location;
			this.type = type;
			this.required = required;
		}

		public String getName() {
			return name;
		}

		public String getLocation() {
			return location;
		}

		public String getType() {
			return type;
		}

		public boolean isRequired() {
			return required;
		}

	}

	private static class SecurityInfo {

		private final String schemeName;

		private final String type;

		private final String credentialName;

		private final String location;

		public SecurityInfo(String schemeName, String type, String credentialName, String location) {
			this.schemeName = schemeName;
			this.type = type;
			this.credentialName = credentialName;
			this.location = location;
		}

		public String getSchemeName() {
			return schemeName;
		}

		public String getType() {
			return type;
		}

		public String getCredentialName() {
			return credentialName;
		}

		public String getLocation() {
			return location;
		}

	}

}
