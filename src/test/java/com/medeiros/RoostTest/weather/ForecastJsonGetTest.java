	private List<Map<String, Object>> loadTestData() {
		try {
			File file = new File(TEST_DATA_FILE);
			if (!file.exists()) {
				logger.error("✗ Test data file not found: {}", TEST_DATA_FILE);
				return new ArrayList<>();
			}
			List<Map<String, Object>> testCases = objectMapper.readValue(file,
					new TypeReference<List<Map<String, Object>>>() {
					});
			logger.info("✓ Loaded {} test cases from {}", testCases.size(), TEST_DATA_FILE);
			return testCases;
		}
		catch (IOException e) {
			logger.error("✗ Failed to load test data from {}: {}", TEST_DATA_FILE, e.getMessage());
			return new ArrayList<>();
		}
	}

