	@BeforeAll
	public void setup() {
		authHandler = new UnifiedAuthHandler();
		responseValidator = new ResponseValidator();
		objectMapper = new ObjectMapper();

		String baseUrl = authHandler.getBaseUrl();
		if (baseUrl == null || baseUrl.isEmpty()) {
			RestAssured.baseURI = BASE_URI;
		}
		else {
			RestAssured.baseURI = baseUrl;
		}

		parameters = getParameters();
		security = getSecurityInfo();
		schemas = getResponseSchemas();

		logger.info("✓ Test setup completed. Base URI: {}", RestAssured.baseURI);
	}

	public Stream<TestScenario> testScenarioProvider() {
		List<Map<String, Object>> testCases = loadTestData();
		if (testCases == null || testCases.isEmpty()) {
			logger.warn("✗ No test cases found in: {}", TEST_DATA_FILE);
			return Stream.empty();
		}

		int[] index = { 0 };
		return testCases.stream().map(testCase -> {
			index[0]++;
			String scenarioId = String.valueOf(index[0]);
			String scenarioDescription = testCase.containsKey("scenario") ? String.valueOf(testCase.get("scenario"))
					: "Test case " + scenarioId;
			String testName = generateTestName(scenarioDescription, testCase);
			return new TestScenario(scenarioId, testName, scenarioDescription, testCase);
		});
	}

	private String generateTestName(String scenario, Map<String, Object> testCase) {
		Object statusCodeObj = testCase.get("expected_statusCode");
		String statusCode = statusCodeObj != null ? String.valueOf(((Number) statusCodeObj).intValue()) : "unknown";

		String sanitized = scenario.replaceAll("[^a-zA-Z0-9\\s]", "").replaceAll("\\s+", "_").toLowerCase();

		if (sanitized.length() > 50) {
			sanitized = sanitized.substring(0, 50);
		}

		return "test_" + sanitized + "_" + statusCode;
	}

	@ParameterizedTest(name = "[{index}] {0}")
	@MethodSource("testScenarioProvider")
	public void testEndpoint(TestScenario scenario) {
		logger.info("▶ Starting test: {} (ID: {})", scenario.testName, scenario.scenarioId);

		Map<String, Object> testCase = scenario.testCase;
		Object statusCodeObj = testCase.get("expected_statusCode");

		if (statusCodeObj == null) {
			logger.error("✗ Missing expected_statusCode in test case: {}", scenario.scenarioId);
			throw new AssertionError("Missing expected_statusCode in test case");
		}

		int expectedStatusCode = ((Number) statusCodeObj).intValue();
		String authValidityType = (expectedStatusCode == 401) ? "invalid" : "valid";

		logger.info("  Expected status: {}, Auth type: {}", expectedStatusCode, authValidityType);

		Response response = executeTest(testCase, parameters, security, authValidityType);

		ResponseSchema schema = schemas.get(expectedStatusCode);
		if (schema == null) {
			schema = schemas.get(0);
		}

		String contentType = response.getHeader("Content-Type");

		if (contentType != null && contentType.contains("application/json")) {
			responseValidator.validateResponse(response, expectedStatusCode, schema);
			logger.info("  ✓ Response validation completed");
		}
		else {
			if (response.getStatusCode() == expectedStatusCode) {
				logger.info("  ✓ Status code matches: {}", expectedStatusCode);
			}
			else {
				throw new AssertionError("Status code mismatch. Expected: " + expectedStatusCode + ", Actual: "
						+ response.getStatusCode());
			}
		}

		logger.info("✓ Test passed: {} (ID: {})", scenario.testName, scenario.scenarioId);
	}

	private Response executeTest(Map<String, Object> testCase, Map<String, ParameterInfo> parameters,
			SecurityInfo security, String authValidityType) {
		Map<String, Object> queryParams = new HashMap<>();
		Map<String, Object> pathParams = new HashMap<>();
		Map<String, Object> headerParams = new HashMap<>();

		for (Map.Entry<String, Object> entry : testCase.entrySet()) {
			String key = entry.getKey();
			Object value = entry.getValue();

			if (key.equals("scenario") || key.equals("expected_statusCode") || key.equals("ScenarioID")
					|| key.equals("TestName") || key.equals("Description") || key.equals("Execution")
					|| key.equals("Validation")) {
				continue;
			}

			if (value == null) {
				continue;
			}

			if (key.equals("headers") && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> headers = (Map<String, Object>) value;
				for (Map.Entry<String, Object> headerEntry : headers.entrySet()) {
					if (headerEntry.getValue() != null) {
						headerParams.put(headerEntry.getKey(), headerEntry.getValue());
					}
				}
				continue;
			}

			if (key.equals("query") && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> query = (Map<String, Object>) value;
				for (Map.Entry<String, Object> queryEntry : query.entrySet()) {
					if (queryEntry.getValue() != null) {
						queryParams.put(queryEntry.getKey(), queryEntry.getValue());
					}
				}
				continue;
			}

			if (key.equals("path") && value instanceof Map) {
				@SuppressWarnings("unchecked")
				Map<String, Object> path = (Map<String, Object>) value;
				for (Map.Entry<String, Object> pathEntry : path.entrySet()) {
					if (pathEntry.getValue() != null) {
						pathParams.put(pathEntry.getKey(), pathEntry.getValue());
					}
				}
				continue;
			}

			ParameterInfo paramInfo = parameters.get(key);
			if (paramInfo != null) {
				String location = paramInfo.getLocation();
				if ("query".equals(location)) {
					queryParams.put(key, value);
				}
				else if ("path".equals(location)) {
					pathParams.put(key, value);
				}
				else if ("header".equals(location)) {
					headerParams.put(key, value);
				}
			}
			else {
				queryParams.put(key, value);
			}
		}

		RequestSpecification request = given().contentType(ContentType.JSON);

		request = authHandler.applyAuth(request, security.getSchemeName(), authValidityType);

		if (!queryParams.isEmpty()) {
			request = request.queryParams(queryParams);
		}

		if (!pathParams.isEmpty()) {
			request = request.pathParams(pathParams);
		}

		if (!headerParams.isEmpty()) {
			for (Map.Entry<String, Object> header : headerParams.entrySet()) {
				request = request.header(header.getKey(), header.getValue());
			}
		}

		logger.info("  Executing {} {} with query params: {}", HTTP_METHOD.toUpperCase(), ENDPOINT, queryParams);

		Response response = request.when().get(ENDPOINT);

		logger.info("  Response status: {}", response.getStatusCode());

		return response;
	}

	private Map<String, ParameterInfo> getParameters() {
		Map<String, ParameterInfo> params = new HashMap<>();
		params.put("q", new ParameterInfo("q", "query", "string", true));
		params.put("days", new ParameterInfo("days", "query", "integer", true));
		params.put("dt", new ParameterInfo("dt", "query", "string", false));
		params.put("unixdt", new ParameterInfo("unixdt", "query", "integer", false));
		params.put("hour", new ParameterInfo("hour", "query", "integer", false));
		params.put("lang", new ParameterInfo("lang", "query", "string", false));
		return params;
	}

