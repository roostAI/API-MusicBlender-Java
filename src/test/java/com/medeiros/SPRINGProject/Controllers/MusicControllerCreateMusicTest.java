	@Test
	@Tag("integration")
	@DisplayName("Scenario 22: Creation under high concurrency")
	public void testCreateMusicUnderConcurrentCalls() throws Exception {
		int threadCount = 10; // TODO adjust concurrency for stress
		String description = "Concurrent Description";
		String name = "Concurrent Name";
		String music = "Concurrent Music";
		int userid = 987;
		doNothing().when(MusicRepo).save(any(MusicModel.class));
		Runnable task = () -> {
			String result = musicController.createMusic(description, name, music, userid);
			assertEquals((String) "Operação Feita", (String) result);
		};
		Thread[] threads = new Thread[threadCount];
		for (int i = 0; i < threadCount; i++) {
			threads[i] = new Thread(task);
		}
		for (Thread t : threads)
			t.start();
		for (Thread t : threads)
			t.join();
		verify(MusicRepo, times(threadCount)).save(any(MusicModel.class));
	}

